import sys

bs = [(1.8, 2.0, 160), (3.5, 3.8, 80), (5.3, 5.4, 60), (7.0, 7.2, 40), (10.1, 10.15, 30), (14.0, 14.35, 20),
      (18.068, 18.168, 17), (21.0, 21.45, 15), (24.89, 24.99, 12), (28.0, 29.7, 10), (50.0, 52.0, 6),
      (144.0, 146.0, 2), (432.0, 436.0, .7), (1260.0, 1300.0, .23)]

f0 = float(sys.argv[1])
print("base frequency: %f" % f0)
print("top frequency: %f" % (f0*200))
for i in range(0,200):
    f = i * f0
    for b in bs:
        if f >= b[0] and f <= b[1]:
            print("hit: %f on harmonics %d, band: %2.2f (%f, %f)" % (f, i, b[2], b[0], b[1]))
